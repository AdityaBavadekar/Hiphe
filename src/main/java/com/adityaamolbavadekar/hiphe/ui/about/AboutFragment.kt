package com.adityaamolbavadekar.hiphe.ui.about

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.TextView
import androidx.fragment.app.Fragment
import com.adityaamolbavadekar.hiphe.R
import com.google.firebase.auth.FirebaseAuth
import com.google.firebase.auth.ktx.auth
import com.google.firebase.ktx.Firebase

class AboutFragment : Fragment() {

    private lateinit var versionTextView: TextView
    private lateinit var auth: FirebaseAuth

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        val root = inflater.inflate(R.layout.about_hiphe_fragment2, container, false)
        auth = Firebase.auth
        versionTextView = root.findViewById(R.id.versiontextView2)

        val pckMangr =
            requireContext().packageManager.getPackageInfo(requireContext().packageName, 0)
        val packageName = pckMangr.packageName
        val versionName = pckMangr.versionName
        val versionCode = pckMangr.versionCode.toString()
        versionTextView.text = versionCode + "(${versionName})"
        return root
    }


}